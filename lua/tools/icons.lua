function GetFileIcon(extension)
	local icon = icons["files"][extension]

	if icon == nil then
		return ''
	else
		return icon["icon"]
	end
end

function GetFileType(extension)
	if extension == 'null' then
		return extension
	end

	local name = icons["files"][extension]

	if name == nil then
		return extension
	else
		return name["name"]
	end
end
